const mongoose = require("mongoose");

const userSchemaSales = new mongoose.Schema({
    firstName : {
        type: String,
        required: true
    },
    lastName : {
        type: String,
        required: true
    },
    email : {
        type: String,
        required: true
    },
    password : {
        type: String,
        required: true
    }
});

mongoose.model("userModelSales", userSchemaSales);